package io.github.kerelape.reikai.typing

/**
 * Type of data.
 *
 * @since 0.1.0
 */
enum class Type {

    /**
     * Strict number.
     *
     * @since 0.1.0
     */
    INTEGER,

    /**
     * Logical value.
     *
     * @since 0.1.0
     */
    BOOLEAN,

    /**
     * Text.
     *
     * @since 0.1.0
     */
    TEXT,
}
